int gcd(int x, int y){
	while(x%=y)	swap(x, y);
	return y;
}

int main(){
    //建立table
	int n, ans[502]={0};
	for(int i=1; i<502; i++){
        //假設i=5，則表示已跑完(2,1)+(3,1)+(3,2)+(4,1)+(4,2)+(4,3)=ans[4]，接下來ans[5]=ans[4]+(5,1)+(5,2)+(5,3)+(5,4)
		ans[i]=ans[i-1];
		for(int j=1; j<i; j++){
			ans[i]+=gcd(i, j);
		}
	}
		
	while(cin >> n && n!=0){
		cout << ans[n] << endl;
	}
	return 0;	
}